<div class="tipSection">
<h3><span class="spaicon-Permissions">&nbsp;</span>Permissions</h3>
This plugin adds 6 new permissions:
<ul>
<li>Can upload images in posts</li>
<li>Can upload media in posts</li>
<li>Can upload files in posts</li>
<li>Can upload signature images</li>
<li>Can download other file type attachments</li>
<li>Can manage their uploaded attachments in profile</li>
</ul>
You will need to visit the forum <b>Permissions Management</b> panels to enable those users you wish to have these Permissions
</div>

