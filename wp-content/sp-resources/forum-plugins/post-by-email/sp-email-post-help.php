<div class="tipSection">
<h3><span class="spaicon-Options">&nbsp;</span>Setup Options</h3>
To set up Post by Email use the Options link below the plugin name or go to the <b>Email Settings</b> panel in the <b>Options</b> section
</div>

<div class="tipSection">
<h3><span class="spaicon-Question">&nbsp;</span>Popup Help</h3>
The popup help in the options panel offers guidance for each section
</div>

<div class="tipSection">
<h3><span class="spaicon-Permissions">&nbsp;</span>Permissions</h3>
This plugin adds 2 new permissions:
<ul>
<li>Can reply to topics using email</li>
<li>Can start new topics using email</li>
</ul>
You will need to visit the forum <b>Permissions Management</b> panels to enable those users you wish to have these Permissions
</div>

<div class="tipSection">
<h3><span class="spaicon-Forums">&nbsp;</span>Adding To Forums</h3>
You will need to visit the <b>Manage Forums</b> panels to add the necessary unique email address to each forum
</div>

