<div class="tipSection">
<h3><span class="spaicon-Permissions">&nbsp;</span>Permissions</h3>
This plugin adds 2 new permissions:
<ul>
<li>Can create linked blog post and forum topics</li>
<li>Can break link between blog posts and forum topics</li>
</ul>
You will need to visit the forum <b>Permissions Management</b> panels to enable those users you wish to have these Permissions
</div>

